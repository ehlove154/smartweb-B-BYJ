public class Food {
    public static String name;
    public int kcal;

    public Food(String name, int kcal) {
        super();
        this.name = name;
        this.kcal = kcal;
    }

    public String getName() {
        return name;
    }

    public int getKcal() {
        return this.kcal;
    }


}
